def default_prompt(role_id: str) -> str:
    role = get_role(role_id)
    base = DEFAULT_ROLE_PROMPTS.get(role_id, "Ты помощник MKG.")
    parts = [base, CHAT_RULES, MULTILINGUAL_NOTE, CHAT_OUTPUT_RULES]
    if role:
        parts.append(f"Роль: {role['name_ru']}.")
    return "\n\n".join(parts)
